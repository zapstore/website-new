	<h2 class="text-2xl font-extra-bold mt-12 mb-4">What is Zapstore?</h2>
	
	<p>
		Zapstore is a decentralized app marketplace built on the nostr protocol. It enables direct connections between developers and users, 
		with cryptographic verification ensuring trust and security without intermediaries.
	</p>
	
	<h2 class="text-2xl font-extra-bold mt-12 mb-4">Key Features</h2>
	
	<ul class="space-y-2">
		<li><strong>Decentralized Distribution</strong> - No single point of control or censorship</li>
		<li><strong>Cryptographic Verification</strong> - Every app is signed and verifiable</li>
		<li><strong>Direct Payments</strong> - Lightning Network micropayments between users and developers</li>
		<li><strong>Social Discovery</strong> - Find apps through your trusted network</li>
		<li><strong>Open Protocol</strong> - Built on nostr, ensuring openness and interoperability</li>
	</ul>
	
